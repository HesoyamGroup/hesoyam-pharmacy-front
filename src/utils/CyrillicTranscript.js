function cyrillic2latin(text){
    if(!text)
        return '';
        
    text = text
        .replace(/\u0410/g, 'A')
        .replace(/\u0411/g, 'B')
        .replace(/\u0412/g, 'V')
        .replace(/\u0413/g, 'G')
        .replace(/\u0414/g, 'D')
        .replace(/\u0402/g, 'Đ')
        .replace(/\u0415/g, 'E')
        .replace(/\u0416/g, 'Ž')
        .replace(/\u0417/g, 'Z')
        .replace(/\u0418/g, 'I')
        .replace(/\u0408/g, 'J')
        .replace(/\u041A/g, 'K')
        .replace(/\u041B/g, 'L')
        .replace(/\u0409/g, 'Lj')
        .replace(/\u041C/g, 'M')
        .replace(/\u041D/g, 'N')
        .replace(/\u040A/g, 'Nj')
        .replace(/\u041E/g, 'O')
        .replace(/\u041F/g, 'P')
        .replace(/\u0420/g, 'R')
        .replace(/\u0421/g, 'S')
        .replace(/\u0422/g, 'T')
        .replace(/\u040B/g, 'Ć')
        .replace(/\u0423/g, 'U')
        .replace(/\u0424/g, 'F')
        .replace(/\u0425/g, 'H')
        .replace(/\u0426/g, 'C')
        .replace(/\u0427/g, 'Č')
        .replace(/\u040F/g, 'Dž')
        .replace(/\u0428/g, 'Š')
        .replace(/\u0430/g, 'a')
        .replace(/\u0431/g, 'b')
        .replace(/\u0432/g, 'v')
        .replace(/\u0433/g, 'g')
        .replace(/\u0434/g, 'd')
        .replace(/\u0452/g, 'đ')
        .replace(/\u0435/g, 'e')
        .replace(/\u0436/g, 'ž')
        .replace(/\u0437/g, 'z')
        .replace(/\u0438/g, 'i')
        .replace(/\u0458/g, 'j')
        .replace(/\u043A/g, 'k')
        .replace(/\u043B/g, 'l')
        .replace(/\u0459/g, 'lj')
        .replace(/\u043C/g, 'm')
        .replace(/\u043D/g, 'n')
        .replace(/\u045A/g, 'nj')
        .replace(/\u043E/g, 'o')
        .replace(/\u043F/g, 'p')
        .replace(/\u0440/g, 'r')
        .replace(/\u0441/g, 's')
        .replace(/\u0442/g, 't')
        .replace(/\u045B/g, 'ć')
        .replace(/\u0443/g, 'u')
        .replace(/\u0444/g, 'f')
        .replace(/\u0445/g, 'h')
        .replace(/\u0446/g, 'c')
        .replace(/\u0447/g, 'č')
        .replace(/\u045F/g, 'dž')
        .replace(/\u0448/g, 'š');

    return text;
}

export default cyrillic2latin